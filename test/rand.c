static void treyfer_crypt(uint8_t *pt, uint8_t *ct)
{
    uint8_t t;
    size_t i;

    memcpy(ct, pt, 8);
    t = ct[0];

    for(i = 0; i < 8*32; i++) {
        t += RANDKEY[i%8];
        t = RANDSBOX[t] + ct[(i+1)%8];
        ct[(i+1) % 8] = t = (t << 1) | (t >> 7);
    }
}

void test_rand(uint8_t *buf, size_t len)
{
    uint8_t pt[8], ct[8], *p, *c, *t;
    size_t i, n, l;

    n = len / 8;

    memcpy(pt, RANDTXT, sizeof(RANDTXT));
    p = pt;
    c = ct;

    for(i = 0; i < n; i++, buf += 8) {
        treyfer_crypt(p, c);
        memcpy(buf, c, 8);
        t = p; p = c; c = t;
    }

    l = len % 8;
    if(l > 0) {
        treyfer_crypt(p, c);
        memcpy(buf, c, l);
    }
}
